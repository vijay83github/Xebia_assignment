package com.zebia.yakshop.model;

public class YakProducts {
	private float milk;
	private float skins;
	/**
	 * @return the milk
	 */
	public float getMilk() {
		return milk;
	}
	/**
	 * @param milk the milk to set
	 */
	public void setMilk(float milk) {
		this.milk = milk;
	}
	/**
	 * @return the skins
	 */
	public float getSkins() {
		return skins;
	}
	/**
	 * @param skins the skins to set
	 */
	public void setSkins(float skins) {
		this.skins = skins;
	}
	

}
